package loops;

public class ForWhileDoWhile {
    public static void main(String[] args) {
        for (int i = 10; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("hlo");
        int j = 10;
        while(j <10){
            System.out.println(j);
            j++;
        }
        System.out.println("hloooo");
        int k = 10;
        do{
            System.out.println(k);
            k++;
        }while (k <10);


    }
}
